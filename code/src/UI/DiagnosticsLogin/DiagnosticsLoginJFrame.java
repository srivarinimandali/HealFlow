    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        Boolean foundUser = false;
        
        String userType = (String) this.combobtnUser.getSelectedItem();
        
        if("admin".equalsIgnoreCase(userType) && this.applicationSystem.getDiagnosticUsersDirectory().userAuthentication(txtUsername.getText(), txtPassword.getText()) != null)
        {
            UserAccount userAccount = this.applicationSystem.getDiagnosticUsersDirectory().userAuthentication(txtUsername.getText(), txtPassword.getText()); 
            foundUser = true;
            userAccount.getUserRole().workAreaCreation(applicationSystem, userAccount);
            this.setVisible(false);   
        }
        
        if("Vendor".equalsIgnoreCase(userType) && (this.applicationSystem.getDiagnosticUsersDirectory().getVendorsUserAccountDirectory().authenticateUser(txtUsername.getText(), txtPassword.getText()) != null)){ 
            
            Vendors vendor = this.applicationSystem.getDiagnosticUsersDirectory().getVendorsUserAccountDirectory().authenticateUser(txtUsername.getText(), txtPassword.getText()); 
            foundUser = true;
            vendor.getUserRole().workAreaCreation(applicationSystem, vendor);
            this.setVisible(false); 
        }
        
        if("Diagnosticians".equalsIgnoreCase(userType) && (this.applicationSystem.getDiagnosticUsersDirectory().getDiagnosticiansUserAccountDirectory().userAuthentication(txtUsername.getText(), txtPassword.getText()) != null)){ 
            
            Diagnosticians diag = this.applicationSystem.getDiagnosticUsersDirectory().getDiagnosticiansUserAccountDirectory().userAuthentication(txtUsername.getText(), txtPassword.getText()); 
            foundUser = true;
            diag.getUserRole().workAreaCreation(applicationSystem, diag);
            this.setVisible(false); 
        }
        
        
        if(!foundUser) {
            JOptionPane.showMessageDialog(null, "Invalid Credentials");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

