     private class MyCellRenderer extends DefaultTableCellRenderer {
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (row % 2 == 0) {
                c.setBackground(new Color(204, 204, 255)); 
            } else {
                c.setBackground(new Color(204, 204, 255)); 
            }
            return c;
        }
    }
    
    public void PopulateTable(){
        RequestTableModel.setRowCount(0);
        
        for(AmbulanceRequest ar : driver.getAmbulanceRequestDirectory().getAmbulanceRequestDetailsList()){
            Object rows[] = new Object[2];
            rows[0] = ar.getPatientDetails().getUserName();
            rows[1] = ar.getPatientAddress();
            
            
            RequestTableModel.addRow(rows);
        }
    }
    
    public void PopulateDoctor(){
        DoctorTableModel.setRowCount(0);
        
        for(Doctor dr : applicationSystem.getHospitalDirectory().getDoctorRegistry().getDoctorUserAccountList()){
            Object rows[] = new Object[1];
            rows[0] = dr.getUserName();
            
            
            
            DoctorTableModel.addRow(rows);
        }
        
    }

