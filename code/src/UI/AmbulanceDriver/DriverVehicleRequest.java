    private void btnDropoffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDropoffActionPerformed
        // TODO add your handling code here:
        int row = tblPatient.getSelectedRow();
        int doctorrow = tblDoctor.getSelectedRow();

        if(row == -1)
        {
            JOptionPane.showMessageDialog(null,"Please select a Patient", "Select Warning", 2);
            return;
        }

        if(doctorrow == -1)
        {
            JOptionPane.showMessageDialog(null,"Please select a Doctor", "Select Warning", 2);
            return;

        }

        AmbulanceRequest ar = driver.getAmbulanceRequestDirectory().getAmbulanceRequestDetailsList().get(row);
        Patient patient = ar.getPatientDetails();
        Doctor doctor = applicationSystem.getHospitalDirectory().getDoctorRegistry().getDoctorUserAccountList().get(doctorrow);

        ar.getAssignedVehiclesDetails().setStatus("Available");
        doctor.getPatientDirectoryDetail().getPatientDetailList().add(patient);
        driver.increaseTrip();
        driver.getAmbulanceRequestDirectory().getAmbulanceRequestDetailsList().remove(row);
        JOptionPane.showMessageDialog(null, "Succesfully dropped of Patient");
    }//GEN-LAST:event_btnDropoffActionPerformed


