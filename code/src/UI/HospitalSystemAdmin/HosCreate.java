    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
          String userName = userNameField.getText();
         String password = passwordField.getText();
       UserValidationService validationService = new UserValidationService();

        
        if(userNameField.getText().isEmpty()||passwordField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "All Fields are Required");
        return;
    }
    
    if (!validationService.isValidUsername(userName)) {
        JOptionPane.showMessageDialog(null, "Invalid username. It should be 3-15 characters, starting with a letter.");
        return;
    }
    
    if (!validationService.isStrongPassword(password)) {
        String passwordMessage = validationService.getPasswordValidationMessage(password);
        JOptionPane.showMessageDialog(null, passwordMessage);
        return;
    }
        try{
            //creating doctor
            if(jComboBox1.getSelectedItem().equals("Doctor")){
                 if(this.applicationSystem.getHospitalDirectory().getDoctorRegistry().checkingUniqueUserName(userName)){
                     this.applicationSystem.getHospitalDirectory().getDoctorRegistry().userAccountCreation(userNameField.getText(), passwordField.getText(), new DoctorRole());
                     //System.out.println("HI"+ this.applicationSystem.getHospitalUserAccountDirectory().getHospitaluseraccountlist().size());
                     JOptionPane.showMessageDialog(null, "Created New Doctor");
                 }else{
                        JOptionPane.showMessageDialog(null, "Doctor with same credentials already exists");
                 }
             }
            //creating Receptionist
            if(jComboBox1.getSelectedItem().equals("Receptionist")){
                 if(this.applicationSystem.getHospitalDirectory().getReceptionistRegistry().checkingUniqueUserName(userName)){
                     this.applicationSystem.getHospitalDirectory().getReceptionistRegistry().userAccountCreation(userNameField.getText(), passwordField.getText(), new ReceptionistRole());
                     JOptionPane.showMessageDialog(null, "Created New Recptionist");
                 }else{
                        JOptionPane.showMessageDialog(null, "Recptionist with same credentials already exists");
                 }
             }
        }catch (Exception e) {
       JOptionPane.showMessageDialog(null, "An error occurred while creating account : " + e.getMessage());
    }
        userNameField.setText("");
        passwordField.setText("");
    }//GEN-LAST:event_createBtnActionPerformed


