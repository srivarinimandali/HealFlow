    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        int diagrow = jTable2.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(null,"Please select a Patient", "Select Warning", 2);
            
        }
        
        if(diagrow == -1){
            JOptionPane.showMessageDialog(null,"Please select a Diagnostician", "Select Warning", 2);
        }
        
        Patient patient = applicationSystem.getPatientUserAccountDirectory().getPatientDetailDirectory().getPatientDetailList().get(row);
        Diagnosticians diag = applicationSystem.getDiagnosticUsersDirectory().getDiagnosticiansUserAccountDirectory().getDiagUserAccountList().get(diagrow);
        
        
        DiagnosticRequest dr = applicationSystem.getPatientUserAccountDirectory().getDiagnosticRequestDirectory().requestDiagnostic(doctor, patient, diag, jTextField1.getText());
        
        diag.getDiagnosticRequestDirectory().getDiagnosticDetailRequestList().add(dr);
        doctor.getDiagnosticRequestDirectoryDetail().getDiagnosticDetailRequestList().add(dr);
        
        JOptionPane.showMessageDialog(null, "Request Submitted Succesfully");
    }//GEN-LAST:event_jButton1ActionPerformed


