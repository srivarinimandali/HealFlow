    private class MyCellRenderer extends DefaultTableCellRenderer {
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (row % 2 == 0) {
               c.setBackground(new Color(204, 204, 255));
            } else {
                c.setBackground(new Color(204, 204, 255));
            }
            return c;
        }
    }
    
    public void populateTable(){
        PatientTableModel.setRowCount(0);
        
        for(Patient pt : doctor.getPatientDirectoryDetail().getPatientDetailList()){
            Object rows[] = new Object[2];
            rows[0] = pt.getUserName();
            rows[1] = pt.getBloodPressue();
            
            
            
            PatientTableModel.addRow(rows);
        }
    }
    
    public void populateDiagTable(){
        DiagTableModel.setRowCount(0);
        
        for(Diagnosticians diag: applicationSystem.getDiagnosticUsersDirectory().getDiagnosticiansUserAccountDirectory().getDiagUserAccountList()){
            Object rows[]= new Object[1];
            rows[0] = diag.getUserName();
            
            
            DiagTableModel.addRow(rows);
        }
    }

