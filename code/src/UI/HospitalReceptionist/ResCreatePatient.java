    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if(userNameField.getText().isEmpty()||passwordField.getText().isEmpty()|| bpField.getText().isEmpty() || ageField.getText().isEmpty() || weightField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"All Fields are required");
        }
        else{
            String userName = userNameField.getText();
            double weight = Double.parseDouble(weightField.getText()); // Parse the weight as double

            if(this.applicationSystem.getPatientUserAccountDirectory().getPatientDetailDirectory().checkUniqueUsername(userName)){
                this.applicationSystem.getPatientUserAccountDirectory().getPatientDetailDirectory().userAccountCreation(userNameField.getText(), passwordField.getText(), new PatientRole(), bpField.getText(), Double.valueOf(ageField.getText()), weight);
                //System.out.println("HI"+ this.applicationSystem.getHospitalUserAccountDirectory().getHospitaluseraccountlist().size());
                JOptionPane.showMessageDialog(null, "Created New Patient");
            }else{
                JOptionPane.showMessageDialog(null, "Patient with same credentials already exists");
            }
        }
        userNameField.setText("");
        passwordField.setText("");
        bpField.setText("");
        ageField.setText("");
        weightField.setText("");
    }//GEN-LAST:event_createBtnActionPerformed

