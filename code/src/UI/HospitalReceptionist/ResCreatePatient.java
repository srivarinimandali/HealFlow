    private void setFakeData() {
        Faker faker = new Faker();
        String fakeUserName = faker.name().username(); // Random username
        String fakePassword = faker.internet().password(); // Random password
        String fakeBp = faker.number().numberBetween(80, 120) + "/" + faker.number().numberBetween(60, 90); // Random BP
        int fakeAge = faker.number().numberBetween(1, 100); // Random age
        double fakeWeight = faker.number().randomDouble(2, 10, 200); // Random weight (with 2 decimal places, between 10 and 200)

        userNameField.setText(fakeUserName);
        passwordField.setText(fakePassword);
        bpField.setText(fakeBp);
        ageField.setText(String.valueOf(fakeAge));
        weightField.setText(String.format("%.2f", fakeWeight));
    }
