  public void populateTable() {
    DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0);

    // Check if the selected item in the combo box is "Doctor"
    if(jComboBox1.getSelectedItem().equals("Store")){

        for(Store store : applicationSystem.getPharmacyUsersDirectory().getStoreDirectoryList().getStoreList()){
            Object[] rows = new Object[3];
            rows[0] = store.getUserName();
            rows[1] = store.getPassword();
            rows[2]=store.getStoreName();
            model.addRow(rows); // Add the row to the model
        }
    }
    else if(jComboBox1.getSelectedItem().equals("Medicine Company")){
        for(Companies company : applicationSystem.getPharmacyUsersDirectory().getCompaniesDirectoryList().getCompanyList()){
            Object[] rows = new Object[3];
            rows[0] = company.getUserName();
            rows[1] = company.getPassword();
            rows[2]=company.getCompanyName();
            model.addRow(rows); // Add the row to the model
        }
    }
}
