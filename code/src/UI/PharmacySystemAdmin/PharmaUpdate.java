    private void createBtn4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtn4ActionPerformed
        int row = jTable2.getSelectedRow();
    if(row == -1){
        JOptionPane.showMessageDialog(null, "Please Select a Row");
        return;
    }
    if(jComboBox1.getSelectedItem().equals("Store")){
        ArrayList<Store> storeList = applicationSystem.getPharmacyUsersDirectory().getStoreDirectoryList().getStoreList();
        Store store = storeList.get(row);
        userNameField.setText(store.getUserName());
        passwordField.setText(store.getPassword());
        Companyname.setText(store.getStoreName());
    } else if(jComboBox1.getSelectedItem().equals("Medicine Company")){
        ArrayList<Companies> companyList = applicationSystem.getPharmacyUsersDirectory().getCompaniesDirectoryList().getCompanyList();
        Companies company = companyList.get(row);
        userNameField.setText(company.getUserName());
        passwordField.setText(company.getPassword());
        Companyname.setText(company.getCompanyName());
    }
    }//GEN-LAST:event_createBtn4ActionPerformed

    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        if(userNameField.getText().isEmpty() || passwordField.getText().isEmpty() || Companyname.getText().isEmpty()){
        JOptionPane.showMessageDialog(null,"All Fields are required");
        return;
    }
    int row = jTable2.getSelectedRow();
    if(row == -1){
        JOptionPane.showMessageDialog(null, "Please Select a Row");
        return;
    }

    String newUserName = userNameField.getText();
    String newPassword = passwordField.getText();
    String companyName=Companyname.getText();
    if(jComboBox1.getSelectedItem().equals("Store")){
        ArrayList<Store> storeList = applicationSystem.getPharmacyUsersDirectory().getStoreDirectoryList().getStoreList();
        Store store = storeList.get(row);
        store.setUserName(newUserName);
        store.setPassword(newPassword);
        store.setStoreName(companyName);
    } else if(jComboBox1.getSelectedItem().equals("Medicine Company")){
        ArrayList<Companies> companyList = applicationSystem.getPharmacyUsersDirectory().getCompaniesDirectoryList().getCompanyList();
        Companies company = companyList.get(row);
        company.setUserName(newUserName);
        company.setPassword(newPassword);
        company.setCompanyName(companyName);
    }

    populateTable();
    JOptionPane.showMessageDialog(null, "Updated");
    userNameField.setText("");
    passwordField.setText("");
    Companyname.setText("");
    }//GEN-LAST:event_createBtnActionPerformed

