    private class MyCellRenderer extends DefaultTableCellRenderer {
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (row % 2 == 0) {
                c.setBackground(new Color(204, 204, 255)); 
            } else {
                c.setBackground(new Color(204, 204, 255)); 
            }
            return c;
        }
    }
    
    public void PopulateTable(){
        VendorTableModel.setRowCount(0);
        
        for(Vendors v : applicationSystem.getDiagnosticUsersDirectory().getVendorsUserAccountDirectory().getVendorUserAccountList()){
            Object rows[] = new Object[3];
            rows[0] = v.getUserName();
            rows[1] = v.getPassword();
            rows[2] = v.getVendorCompany();
            
            VendorTableModel.addRow(rows);
        }
    }
    
    public void PopulateDiag(){
        DiagTableModel.setRowCount(0);
        
        for(Diagnosticians d : applicationSystem.getDiagnosticUsersDirectory().getDiagnosticiansUserAccountDirectory().getDiagUserAccountList()){
            Object rows[] = new Object[2];
            rows[0] = d.getUserName();
            rows[1] = d.getPassword();

            DiagTableModel.addRow(rows);
        }
    }

