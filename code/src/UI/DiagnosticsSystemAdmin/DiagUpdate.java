    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if(experienceField.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null,"All Fields are Compulsory");
        }
        else{
            int row = jTable3.getSelectedRow();
            Vendors vendor = applicationSystem.getDiagnosticUsersDirectory().getVendorsUserAccountDirectory().getVendorUserAccountList().get(row);

            vendor.setVendorCompany(experienceField.getText());

            PopulateTable();

            JOptionPane.showMessageDialog(null, "Updated");
        }
        experienceField.setText("");
    }//GEN-LAST:event_createBtnActionPerformed

