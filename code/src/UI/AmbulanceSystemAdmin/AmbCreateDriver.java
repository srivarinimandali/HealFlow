    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
             
    String userName = txtUserName.getText();
    String password = txtPassword.getText();
    String ageText = txtAge.getText();
    String experienceText = txtExperience.getText();
    UserValidationService validationService = new UserValidationService();

    if(userName.isEmpty() || password.isEmpty() || ageText.isEmpty() || experienceText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "All Fields are Compulsory");
        return;
    }
    
    if (!validationService.isValidUsername(userName)) {
        JOptionPane.showMessageDialog(null, "Invalid username. It should be 3-15 characters, starting with a letter.");
        return;
    }
    
    if (!validationService.isStrongPassword(password)) {
        String passwordMessage = validationService.getPasswordValidationMessage(password);
        JOptionPane.showMessageDialog(null, passwordMessage);
        return;
    }
    
    try {
        int experience = Integer.parseInt(experienceText);
        double age = Double.parseDouble(ageText);

        // Extract the vehicle number from the selected item in comboboxVehicle

        if(this.applicationSystem.getAmbulanceDirectory().getDriverUserDirectory().checkingUniqueUserName(userName)) {
            this.applicationSystem.getAmbulanceDirectory().getDriverUserDirectory().userAccountCreation(userName, password, new AmbulanceDriverRole(), age, String.valueOf(experience));
            JOptionPane.showMessageDialog(null, "Created New Ambulance Driver");
        } else {
            JOptionPane.showMessageDialog(null, "Ambulance Driver with same credentials already exists");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Experience, Age, and Vehicle Number must be valid numbers");
    }
     txtUserName.setText("");
     txtPassword.setText("");
     txtAge.setText("");
     txtExperience.setText("");
    }//GEN-LAST:event_btnCreateActionPerformed


