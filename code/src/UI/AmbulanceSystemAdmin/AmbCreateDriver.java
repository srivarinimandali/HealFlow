    private void setFakeData() {
        Faker faker = new Faker();
        String fakeUserName = faker.name().username(); // Random username
        String fakePassword = faker.internet().password(8, 15); // Random password
        String fakeAge = String.valueOf(faker.number().numberBetween(20, 60)); // Random age between 20 and 60
        String fakeExperience = String.valueOf(faker.number().numberBetween(1, 10)); // Random experience between 1 and 30

        txtUserName.setText(fakeUserName);
        txtPassword.setText(fakePassword);
        txtAge.setText(fakeAge);
        txtExperience.setText(fakeExperience);
    }
